# 算術演算子
    # +： 加算
        # 例： '10' + '3' = '103'
        # 例： 'こんにちは、' + '赤ちゃん' = 'こんにちは、赤ちゃん'
        # 例： 1 + 1 = 2
        # 例： 15 + int('15') = 30
        # 型が一致していないとエラーになる
    # -： 減算
    # *: 乗算
    # /: 除算
        # 例： 5 / 3 = 1.6666666667
        # 例： 5 // 3 = 1
        # 例： 5 % 3 = 2 
        # 応用： divmod(5, 3) = (1, 2)

    # **: べき乗
    # //: 除算(切り捨て)
    # %: 剰余(あまり)